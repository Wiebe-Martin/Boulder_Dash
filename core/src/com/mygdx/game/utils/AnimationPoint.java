package com.mygdx.game.utils;

public class AnimationPoint {
        private int x;
        private int y;

        public AnimationPoint(int x, int y) {
            this.x = x;
            this.y = y;
        }

        public int getX() {
            return x;
        }

        public int getY() {
            return y;
        }
        
    }
